# lists
fruits = ["apple", "banana", "cherry"]
print(fruits)

# tuples
fruits = ("apple", "banana", "cherry")
print(fruits)

# sets
fruits = {"apple", "banana", "cherry"}
print(fruits)

# dictionaries
person = {"name": "John", "age": 30, "city": "New York"}
print(person)

# len() function
fruits = ["apple", "banana", "cherry"]
print(len(fruits))

# indexing and slicing
fruits = ["apple", "banana", "cherry"]
print(fruits[0])
print(fruits[1:3])

# append() function
fruits = ["apple", "banana", "cherry"]
fruits.append("orange")
print(fruits)

# remove() function
fruits = ["apple", "banana", "cherry"]
fruits.remove("banana")
print(fruits)
